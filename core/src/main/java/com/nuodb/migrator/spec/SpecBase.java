package com.nuodb.migrator.spec;

import com.nuodb.migrator.utils.ObjectUtils;

import java.util.Map;

import static com.google.common.collect.Maps.newHashMap;

public class SpecBase implements Spec {

    private String id;
    private String type;
    private Map<String, Object> attributes = newHashMap();

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Map<String, Object> getAttributes() {
        return attributes;
    }

    public void setAttributes(Map<String, Object> attributes) {
        this.attributes = attributes;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o)
            return true;
        if (o == null || getClass() != o.getClass())
            return false;

        SpecBase specBase = (SpecBase) o;

        if (id != null ? !id.equals(specBase.id) : specBase.id != null)
            return false;
        if (attributes != null ? !attributes.equals(specBase.attributes) : specBase.attributes != null)
            return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id != null ? id.hashCode() : 0;
        result = 31 * result + (type != null ? type.hashCode() : 0);
        result = 31 * result + (attributes != null ? attributes.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return ObjectUtils.toString(this);
    }
}
